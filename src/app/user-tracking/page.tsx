// Format sessions for use in the player
const formatSessions = (data: any) => {
  if (!data || !data.data || !data.data.data) return [];

  return data.data.data
    .map((sessionData: IUserTrackingData[], index: number) => {
      if (!sessionData || sessionData.length === 0) return null;

      // Sort events chronologically within each session (past to future)
      const sortedSessionData = [...sessionData].sort(
        (a, b) =>
          new Date(a.timestamp).getTime() - new Date(b.timestamp).getTime()
      );

      const firstEvent = sortedSessionData[0];
      const firstEventTime = new Date(firstEvent.timestamp).getTime();
      const lastEventTime = new Date(
        sortedSessionData[sortedSessionData.length - 1].timestamp
      ).getTime();

      // Calculate duration in format MM:SS
      const durationInMs = lastEventTime - firstEventTime;
      const durationInSeconds = Math.floor(durationInMs / 1000);
      const minutes = Math.floor(durationInSeconds / 60);
      const seconds = durationInSeconds % 60;

      return {
        id: firstEvent.uuid,
        events: sortedSessionData,
        domain: firstEvent.domain,
        path: firstEvent.path,
        date: format(parseISO(firstEvent.timestamp), "yyyy-MM-dd"),
        duration: `${minutes}:${seconds.toString().padStart(2, "0")}`,
      };
    })
    .filter(Boolean);
};

